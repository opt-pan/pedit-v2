function trans_text(button_color, button_text, label_text, css_fontsize, css_width) {
    if (document.getElementById("english").value === "English") {
        var a = 1;
    } else {
        var a = 0;
    }
    for (var i in button_text) {
        document.getElementById(i).value = button_text[i][a];
    }
    for (var i in label_text) {
        document.getElementById(i).innerHTML = label_text[i][a];
    }
    for (var i in css_fontsize) {
        document.getElementById(i).style["font-size"] = css_fontsize[i][a];
    }
    for (var i in css_width) {
        document.getElementById(i).style["width"] = css_width[i][a];
    }
    if (pu.mmode === "solve") {
        document.getElementById("title").innerHTML = ["解答モード", "Solve Mode"][a];
        document.getElementById("tb_delete").value = ["解答消去", "Delete"][a];
    } else {
        document.getElementById("title").innerHTML = ["編集モード", "Edit Mode"][a];
        document.getElementById("tb_delete").value = ["問題・解答消去", "Delete"][a];
    }

    for (var i in button_color) {
        document.getElementById(i).style.backgroundColor = button_color[i][0];
        document.getElementById(i).style.color = button_color[i][1];
    }

}

